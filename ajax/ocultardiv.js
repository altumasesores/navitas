// JavaScript Document

//Funcion para mostrar una capa div
function mostrardiv(capa1, capa2) {

div = document.getElementById(capa1);

div.style.display = '';

div2 = document.getElementById(capa2);

div2.style.display = '';



}

//Funcion para ocultar una capa div
function cerrarDiv(capa1, capa2) {

div = document.getElementById(capa1);

div.style.display='none';

div2 = document.getElementById(capa2);

div2.style.display='none';

}
